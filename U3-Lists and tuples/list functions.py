list=[2,1,3,]
(list.append(4))   #adds one element at the end
print(list)
list.sort()    #arranges list elements in ascending order
print(list)
list.sort(reverse=True)  #sorts in descending order
print(list)
(list.reverse)         #reverses the list
print(list)        
list.insert(1,5)  #inserts element at index     list.insert(index,element)
print(list)
list.remove(1)     #removes first occurence of given element
print(list)
list.pop(3)      #removes element at given index
print(list)
